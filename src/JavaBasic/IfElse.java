package JavaBasic;

public class IfElse {

	public static void main(String[] args) {
		
		int x=30;
		if(x==10) {
			System.out.println("the value of x is 10");
		}else if(x==40) {
			System.out.println("the value of x is 40");
		}else if (x==20) {
			System.out.println("the value of x is 20");
		}else {
			System.out.println("this is else statemnt");
		}

	}

}
